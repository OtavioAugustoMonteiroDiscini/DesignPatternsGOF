public class LetterEntry {
    public final char symbol;
    public final String font;
    public final int size;
    public final String style;
    public final int x;
    public final int y;

    public LetterEntry(char symbol, String font, int size, String style, int x, int y) {
        this.symbol = symbol;
        this.font = font;
        this.size = size;
        this.style = style;
        this.x = x;
        this.y = y;
    }
}
